//整理合作伙伴的边框
function Partners()
{
	$(".Partners").find("li:lt(6) a").css("borderTopColor",'#fff')
		.end().find("li:nth-of-type(6n+1) a").css("borderLeftColor",'#fff')
		.end().find("li:nth-of-type(6n+6) a").css("borderRightColor",'#fff')
		.end().find("li:gt(17) a").css("borderBottomColor",'#fff')
	;
}
