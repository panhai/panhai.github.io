// ====== 初始化下拉框:地铁 站点 ======
// city：字符串，城市下拉框ID
// metrp：字符串，线路下拉框ID
// station：字符串，站点下拉框ID
// iniStation：字符串，默认选中站点代码
// showNull：布尔值，是否显示空项
function InitMetroSelect(city, metro, station, iniStation, showNull) {
    metroSelect[0, 0] = city;
    metroSelect[0, 1] = metro;
    metroSelect[0, 2] = station;
    document.getElementById(metro).length = 0;
    document.getElementById(station).length = 0;
    var ss = document.getElementById(metroSelect[1]);
    var arr = GetCityMetros(document.getElementById(metroSelect[0, 0]).value);
    if (showNull) {
        arr.unshift(["", "", "", "", ""]);
    }
    with (ss) {
        length = 0;
        for (var i = 0; i < arr.length; i++) {
            options[length] = new Option(arr[i][0], arr[i][2]);
        }
        if (options[0]) {
            options[0].selected = true;
        }
    }
    for (i = 0; i < metroSelect.length - 1; i++) {
        document.getElementById(metroSelect[i]).onchange = new Function("MetroChange(" + (i + 1) + ")");
    }
    MetroChange(1, selMetro, showNull);
    
    var selMetro = new Array();
    if (iniStation) {
        selMetro[0] = iniStation.substr(0, 4) + "00000";
        selMetro[1] = iniStation.substr(0, 6) + "00";
        selMetro[2] = iniStation;
        document.getElementById(metroSelect[1]).value = selMetro[1];
    }
    MetroChange(2, selMetro);
}

