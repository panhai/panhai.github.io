function MetroChange(v, selMetro, showNull) {
    if (v > 0) {
        var ss = document.getElementById(metroSelect[v]);
        with (ss) {
            length = 0;
            var arr = GetMetros(document.getElementById(metroSelect[v - 1]).value);
            if (showNull) {
                arr.unshift(["", "", "", "", ""]);
            }
            for (var i = 0; i < arr.length; i++) {
                options[length] = new Option(arr[i][0], arr[i][2]);
            }
            if (options[0]) {
                options[0].selected = true;
            }
            if (selMetro && selMetro[v]) {
                value = selMetro[v];
            }
        }
        if (++v < metroSelect.length) {
            MetroChange(v, selMetro);
        }
    }
}

